<style scoped>
div {
	position: fixed;
	top: 0;
	bottom: 0;
	right: 0;
	left: 0;
	z-index: 100;
	background-color: rgba(255, 255, 255, 0.4);

	display: flex;
	justify-content: center;
	align-items: center;
}
</style>

<template>
	<div v-if=shown>
		<el-progress type=circle :percentage="Math.round(progress)" status=text>{{ status }}</el-progress>
		<a ref=a :href=url :type=type :download=name />
	</div>
</template>

<script>
import Vue from 'vue';
import QRCode from 'qrcode';
import JSZip from 'jszip';


async function tick() {
	await new Promise((resolve, reject) => Vue.nextTick(resolve));
}


export async function oneQRToDataURL(data, margin, background, foreground) {
	return await QRCode.toDataURL(data, {
		color: {
			light: background,
			dark: foreground,
		},
		margin: margin,
		scale: 8,
	});
}


export async function allQRToDataURL(data, margin, background, foreground, callback) {
	const zip = new JSZip();

	zip.file('batch-qr', '', {dir: true});

	for (let i=0; i<data.length; i++) {
		callback(i/data.length * 50, 'generating');
		try {
			const url = await oneQRToDataURL(data[i], margin, background, foreground);

			zip.file(`batch-qr/${i + 1}.png`, url.replace(/.*?,/, ''), {base64: true, comment: data[i]});
		} catch {
		}
	}

	await tick();

	const base64 = await zip.generateAsync({type: 'base64', compression: 'DEFLATE', comment: 'Generated by MacRat/BacthQR'}, ({percent, currentFile}) => {
		callback(percent/2 + 50, 'compressing');
	})

	callback(100, 'downloading');

	return 'data:application/zip;base64,' + base64;
}


export default {
	data() {
		return {
			shown: false,
			progress: 0,
			status: '',
			url: '',
			type: 'application/zip',
			name: 'batch-qr',
		};
	},
	methods: {
		async _prepare(name, type) {
			this.status = 'generating';
			this.progress = 0;
			this.shown = true;
			this.type = type;
			this.name = name;

			await tick();
		},
		async _download() {
			this.status = 'downloading';
			this.progress = 100;

			await tick();

			this.$refs.a.click();

			this.shown = false;
		},
		async downloadIt(data, name, options) {
			await this._prepare(name, 'image/png');

			this.url = await oneQRToDataURL(
				data,
				options.margin,
				options.color.light,
				options.color.dark,
			);

			await this._download();
		},
		async downloadAll(data, options) {
			await this._prepare('batch-qr', 'application/zip');

			this.url = await allQRToDataURL(
				data,
				options.margin,
				options.color.light,
				options.color.dark,
				(progress, status) => {
					this.progress = progress;
					this.status = status;
				},
			);

			await this._download();
		},
	},
};
</script>
